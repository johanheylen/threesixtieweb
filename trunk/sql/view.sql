    (p.ID),
    p.Reviewer,
    p.Reviewee,
    p.Status,
    p.Batch
  FROM poll p
    JOIN preferred_poll pf ON p.Reviewer = pf.Reviewer
  WHERE p.Reviewer != p.Reviewee
        AND pf.user = p.Reviewee
