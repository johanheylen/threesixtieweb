FROM poll p
WHERE
    p.Status = (SELECT ID FROM poll_status WHERE Name='Ingestuurd')
    AND
    p.Reviewee != p.Reviewer
	/*the own review is not added here -- this is correct*/
GROUP BY p.Reviewee;

