CREATE OR REPLACE VIEW average_score_view AS
  SELECT
    p.Reviewee                 AS Reviewee,
    a.Question                 AS Question,
    TRUNCATE(AVG(a.Answer), 2) AS Average_Score,
    p.Batch                    AS Batch,
    p.ID                       AS Poll
  FROM answer a
    JOIN poll p
      ON a.Poll = p.ID
  WHERE Reviewee != Reviewer AND a.Answer != 6
  GROUP BY Question, Reviewee;

