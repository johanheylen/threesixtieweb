CREATE OR REPLACE VIEW preferred_reviewers_view AS
SELECT count(*) AS Aantal_Preferred_Reviewers, p.Reviewee
FROM poll p
JOIN preferred_poll pf ON p.Reviewer = pf.Reviewer
WHERE p.Reviewer != p.Reviewee
GROUP BY p.Reviewee;
